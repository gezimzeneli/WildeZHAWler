public class Bock {
    private int score;
    private int id;

    public Bock(int score, int id) {
        this.score = score;
        this.id = id;
    }
    public Bock(){}

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
